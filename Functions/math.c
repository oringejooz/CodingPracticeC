#include <stdio.h>
#include <math.h>

int main()
{
    printf("%.2f\n",sqrt(256));//Square root
    //Round a number

    printf("%.2f\n",ceil(2.4));
    printf("%.2f\n",floor(2.4));

    //Power of x raised to y  pow(x,y)

    printf("%.2f\n",pow(3,3));

    //Absolute value

    printf("%d\n",abs(-6));

    return 0;
}